var numRookCaptures = function (board) {
    let m = 0;
    let n = 0;

    for (let i = 0; i < board.length; i++) {

        const idx = board[i].indexOf('R')
        if (idx > -1) {
            m = i;
            n = idx;
            break
        }
    }

    let strM = board[m].join('')
    let strN = ''

    for (let i = 0; i < board.length; i++) {
        strN += board[i][n]
    }
    let count = 0

    if (/R\.*p/.test(strM)) {
        count++
    }

    if (/p\.*R/.test(strM)) {
        count++
    }

    if (/R\.*p/.test(strN)) {
        count++
    }
    if (/p\.*R/.test(strN)) {
        count++
    }

    return count

};


