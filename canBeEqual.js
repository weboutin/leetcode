var canBeEqual = function (target, arr) {

    for (let i = 0; i < target.length; i++) {

        for (let j = i; j < arr.length; j++) {

            if (target[i] == arr[j]) {
                let tmp = arr[i]
                arr[i] = target[i]
                arr[j] = tmp
            }
        }
    }

    for (let i = 0; i < target.length; i++) {
        if (target[i] != arr[i]) {
            return false
        }
    }

    return true
};

